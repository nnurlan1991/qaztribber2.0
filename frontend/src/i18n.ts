const dicts: Record<Lang, Dict> = { ru, kz };

export function translate(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  const dict = dicts[lang] ?? ru;
  let value = dict[key] ?? ru[key] ?? key;
  if (vars) for (const [k, v] of Object.entries(vars)) value = value.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
  return value;
}
